#!/usr/bin/env node
/**
 * Prepares the dashboard for deploy or local preview.
 *
 * - Writes gitignored dashboard/build-id.txt and dashboard/config.js
 * - Builds .dashboard-dist/ with cache-busted ?v=<build-id> on local CSS/JS/SVG
 *
 * Build ID: CF_PAGES_COMMIT_SHA → GITHUB_SHA → git short HEAD → "dev"
 *
 * PST_API_URL: Cloudflare Pages BUILD env, or .env locally (not Worker).
 */

import { cp, mkdir, readFile, writeFile, readdir, stat } from "node:fs/promises";
import { execSync } from "node:child_process";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "../..");
const srcDir = join(root, "dashboard");
const outDir = join(root, ".dashboard-dist");

const SKIP = new Set(["config.js", "build-id.txt"]);

function resolveBuildId() {
  const env = process.env.CF_PAGES_COMMIT_SHA ?? process.env.GITHUB_SHA;
  if (env) {
    const normalized = env.replace(/[^a-f0-9]/gi, "");
    if (normalized) return normalized.slice(0, 12);
  }
  try {
    return execSync("git rev-parse --short HEAD", { encoding: "utf8" }).trim();
  } catch {
    return "dev";
  }
}

function bustLocalAssets(html, buildId) {
  return html.replace(
    /(\s(?:href|src)=)"((?!https?:\/\/|\/\/)([^"#?]+\.(?:css|js|svg)))"/gi,
    (_, prefix, url) => `${prefix}"${url}?v=${buildId}"`
  );
}

function configJs(buildId, apiUrl) {
  return `// Generated by scripts/dashboard/prepare-dashboard.mjs — do not edit by hand.
// Source: PST_API_URL (Pages BUILD env) + git/CI commit for cache busting.

window.PST_BUILD_ID = ${JSON.stringify(buildId)};
window.PST_API_URL = ${JSON.stringify(apiUrl)};
`;
}

async function copyEntry(src, dest, buildId) {
  const info = await stat(src);
  if (info.isDirectory()) {
    await mkdir(dest, { recursive: true });
    const entries = await readdir(src);
    for (const name of entries) {
      await copyEntry(join(src, name), join(dest, name), buildId);
    }
    return;
  }

  await mkdir(dirname(dest), { recursive: true });
  if (src.endsWith(".html")) {
    const html = await readFile(src, "utf8");
    await writeFile(dest, bustLocalAssets(html, buildId), "utf8");
  } else {
    await cp(src, dest);
  }
}

async function prepareDashboard() {
  const buildId = resolveBuildId();
  const apiUrl = (process.env.PST_API_URL ?? "").trim().replace(/\/$/, "");

  await writeFile(join(srcDir, "build-id.txt"), `${buildId}\n`, "utf8");
  await writeFile(join(srcDir, "config.js"), configJs(buildId, apiUrl), "utf8");

  await mkdir(outDir, { recursive: true });
  const entries = await readdir(srcDir);

  for (const name of entries) {
    if (SKIP.has(name)) continue;
    await copyEntry(join(srcDir, name), join(outDir, name), buildId);
  }

  await writeFile(join(outDir, "config.js"), configJs(buildId, apiUrl), "utf8");

  console.log(
    `Prepared .dashboard-dist (build=${buildId}${
      apiUrl ? `, PST_API_URL=${apiUrl}` : ", PST_API_URL unset (browser fallback: api.<host> on custom domains)"
    })`
  );
}

await prepareDashboard();
